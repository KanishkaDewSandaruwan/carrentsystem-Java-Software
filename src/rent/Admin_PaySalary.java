    public void autoId(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
            Statement s = con.createStatement(); 
            
            ResultSet rs = s.executeQuery("select Max(salary_payment_id) from salary_pay");
            rs.next();
            rs.getString("Max(salary_payment_id)");
            
            if (rs.getString("Max(salary_payment_id)")==null) {
                txtPaymentID.setText("SP001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(salary_payment_id)").substring(2,rs.getString("Max(salary_payment_id)").length()));
                id++;
                txtPaymentID.setText("SP0" + String.format("%02d", id));
            }
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Admin_PaySalary.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
