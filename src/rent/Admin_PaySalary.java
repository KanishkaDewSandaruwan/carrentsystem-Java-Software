    private void txtOTHKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOTHKeyPressed


        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            String ot = txtOTH.getText();
            String basic = txtBasic.getText();
            
            int oth = Integer.parseInt(ot);
            int basics = Integer.parseInt(basic);
            int amout = oth * 120;
            
            txtOTAmount.setText(String.valueOf(amout));
            
            int salary = amout + basics;
            
            txtSalary.setText(String.valueOf(salary));
            
        }
    }//GEN-LAST:event_txtOTHKeyPressed

    
