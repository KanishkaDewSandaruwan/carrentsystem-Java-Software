    public void autoId(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
            Statement s = con.createStatement(); 
            
            ResultSet rs = s.executeQuery("select Max(rent_id) from rent");
            rs.next();
            rs.getString("Max(rent_id)");
            
            if (rs.getString("Max(rent_id)")==null) {
                txtRentID.setText("R0001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(rent_id)").substring(2,rs.getString("Max(rent_id)").length()));
                id++;
                txtRentID.setText("R0" + String.format("%03d", id));
            }
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(RentCar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
