    private void comboCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCarActionPerformed

//       String id = "U0001";
        String id = comboCar.getSelectedItem().toString();
//            
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
            
            ps1 = (PreparedStatement) con.prepareStatement("select * from carreg where car_reg = ?");
            ps1.setString(1, id);
            
            ResultSet ra = ps1.executeQuery();
            

                
            if (ra.next() == false) {
                JOptionPane.showMessageDialog(this, "Car is Not Available");
            }else{
                String avl = ra.getString("available");
                txtAvailable.setText(avl.trim());
                
                String avlb = ra.getString("rent_fee");
                txtCarFee.setText(avlb.trim());
                
                String avlb1 = ra.getString("car_number");
                txtCarNumber.setText(avlb1.trim());
                
                if (avl.equals("Yes")) {
//                    txtCusID.setEnabled(false);
                    txtCusName.setEnabled(true);
                    txtCusID.setEnabled(true);
                    txtDueDate.setEnabled(true);
                    txtTotle.setEnabled(true);
                    txtPaid.setEnabled(true);
                    txtBalance.setEnabled(true);
                    txtDate.setEnabled(true);
                    txtCarFee.setEnabled(true);
                    txtCarNumber.setEnabled(true);
                    btnCancel1.setEnabled(true);
                    
                    
                    txtTotle.setText("");
                    txtPaid.setText("");
                    txtBalance.setText("");
                    
                    btnAdd2.setEnabled(false);
                    
                }else{
                    txtCarNumber.setEnabled(false);
                    txtCusID.setEnabled(false);
                    txtCusName.setEnabled(false);
                    txtDueDate.setEnabled(false);
                    txtTotle.setEnabled(false);
                    txtPaid.setEnabled(false);
                    txtBalance.setEnabled(false);
                    txtDate.setEnabled(false);
                    txtCarFee.setEnabled(false);
                    btnCancel1.setEnabled(false);
                    btnAdd2.setEnabled(false);
                }
            }
                   
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RentCar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(RentCar.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_comboCarActionPerformed

