    private void txtCusIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCusIDKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            String Cust_ID = txtCusID.getText();
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
                ps = (PreparedStatement) con.prepareStatement("select * from customer where customer_id = ?");
                ps.setString(1, Cust_ID);
                
                ResultSet r = ps.executeQuery();
                
                if (r.next() == false) {
                    JOptionPane.showMessageDialog(this, "Customer is Not Available");
                    txtCusName.setText("");
                }else{
                    String avl = r.getString("name");
                    txtCusName.setText(avl.trim());

                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(RentCar.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(RentCar.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
    }//GEN-LAST:event_txtCusIDKeyPressed

