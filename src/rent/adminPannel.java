    public void loadChart(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
            pst = (PreparedStatement) con.prepareStatement("select * from income where year = ?");
            pst.setString(1, "2020");
            ResultSet rs = pst.executeQuery();
            
            
            DefaultCategoryDataset dataset = new DefaultCategoryDataset();
            String year = null;
            
            while (rs.next()){
                
                String income = rs.getString("total_income");
                String month = rs.getString("month");
                year  = rs.getString("year");
                
                int incomei = Integer.parseInt(income);
                
                dataset.setValue(incomei, "Month", year +"."+ month);
                
            }
            
            JFreeChart chart = ChartFactory.createBarChart(year+" Income Report", "Month", "Income", dataset, PlotOrientation.VERTICAL, true, true, false);
            CategoryPlot plot = chart.getCategoryPlot();
            plot.setRangeGridlinePaint(Color.BLACK);
            
//            ChartFrame farme = new ChartFrame("Bar Char for "+year+" Income",chart);
//            farme.setVisible(true);
//            farme.setSize(650,550);
            
            
            ChartPanel chartpan = new ChartPanel(chart);
            chartPannel.removeAll();
            chartPannel.add(chartpan);
            chartPannel.updateUI();
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Chart.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
