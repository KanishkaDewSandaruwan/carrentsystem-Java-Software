    private void btnAdd2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd2ActionPerformed

        PreparedStatement ps;
        PreparedStatement pb;

        String Extend_ID = txtExtendID.getText();
        String Rent_ID = txtRentID.getText();
        String CarNumber = txtCaeNumber.getText();
        String Duedate = txtDueDate.getText(); 
        String Aditional = txtAditional.getText();
        String Last = txtLast.getText();
        String paid = txtPaid.getText();
        String bLast_bal = txtBalance.getText();
        
        

        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String extend_date = date_format.format(CalDate.getDate());


        if (extend_date.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Customer");
        }else{
            try {

                Class.forName("com.mysql.jdbc.Driver");
                con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");

                pst = (PreparedStatement) con.prepareStatement("insert into extend_rent(extend_id,rent_id,car_number,due_date,extend_date,aditional_amount,last_amount) values(?,?,?,?,?,?,?)");
                pst.setString(1, Extend_ID);
                pst.setString(2, Rent_ID);
                pst.setString(3, CarNumber);
                pst.setString(4, Duedate);
                pst.setString(5, extend_date);
                pst.setString(6, Aditional);
                pst.setString(7, Last);

                ps = (PreparedStatement) con.prepareStatement("update rent set  due_date=?, totle_fee=?, balance=?, extend=? where rent_id=?");
                ps.setString(1, extend_date);
                ps.setString(2, Last);
                ps.setString(3, bLast_bal);
                ps.setString(4, "Yes");
                ps.setString(5, Rent_ID);
                
                pb = (PreparedStatement) con.prepareStatement("update rent_backup set  due_date=?,totle_fee=?,balance=?,extend=? where rent_id=?");
                pb.setString(1, extend_date);
                pb.setString(2, Last);
                pb.setString(3, bLast_bal);
                pb.setString(4, "Yes");
                pb.setString(5, Rent_ID);

                pst.executeUpdate();
                ps.executeUpdate();
                pb.executeUpdate();

                JOptionPane.showMessageDialog(this, "Car Rent Extended Success");
                get_table();
                get_extend_table();
                autoId();
                

                txtRentID.setText("");
                txtCaeNumber.setText("");
                txtDueDate.setText("");
                txtAditional.setText("");
                txtLast.setText("");
                txtPaid.setText("");
                txtBalance.setText("");
                

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CarReg.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(CarReg.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAdd2ActionPerformed

