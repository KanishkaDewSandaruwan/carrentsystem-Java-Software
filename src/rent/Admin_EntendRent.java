    public void autoId(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
            Statement s = con.createStatement(); 
            
            ResultSet rs = s.executeQuery("select Max(extend_id) from extend_rent");
            rs.next();
            rs.getString("Max(extend_id)");
            
            if (rs.getString("Max(extend_id)")==null) {
                txtExtendID.setText("E0001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(extend_id)").substring(2,rs.getString("Max(extend_id)").length()));
                id++;
                txtExtendID.setText("E0" + String.format("%03d", id));
            }
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Admin_EntendRent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
