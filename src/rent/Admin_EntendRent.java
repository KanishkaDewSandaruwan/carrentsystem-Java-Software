    public int countTotle(int days, int amount){
        int fee = 0;
        fee = days * amount;   
        return fee;
    }
    
    public int countBalance(int totle, int paid){
        int fee = 0;
        fee = totle - paid;   
        return fee;
    }
    public int countLastTotle(int totle, int aditional){
        int fee = 0;
        fee = totle + aditional;   
        return fee;
    }
    
