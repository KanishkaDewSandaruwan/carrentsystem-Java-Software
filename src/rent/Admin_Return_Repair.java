    private void txtRepairKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRepairKeyPressed


        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

        String id = txtRepair.getText();
        //
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");

            ps1 = (PreparedStatement) con.prepareStatement("select * from repair where repair_id = ?");
            ps1.setString(1, id);

            ResultSet ra = ps1.executeQuery();

            Date date = new Date();
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy.MM.dd HH:mm:ss");
            String cdate = formatter.format(date);

            if (ra.next() == false) {
                JOptionPane.showMessageDialog(this, "Car is Not Available");
            }else{
                if (ra.getString("finish").equals("No")) {
                    String avlb1 = ra.getString("car_id");
                    txtCarID.setText(avlb1.trim());

                    String sd = ra.getString("car_number");
                    txtCarNumber.setText(sd.trim());

                    String rp = ra.getString("send_date");
                    txtSendDate.setText(rp.trim());

                    txtDate.setText(cdate.trim());

                    txtRepair.setEnabled(true);
                    txtSendDate.setEnabled(true);
                    txtCarNumber.setEnabled(true);
                    btnAdd2.setEnabled(true);
                    txtDate.setEnabled(true);
                    
                    txtCarNumber.setEnabled(true);
                    txtCarID.setEnabled(true);

                    txtBileNumber.setEnabled(true);
                    txtSendDate.setEnabled(true);
                    txtRPlace.setEnabled(true);
                    txtRAmount.setEnabled(true);
                }else{
                    JOptionPane.showMessageDialog(this, "This Car Not Sent Repair");
                }
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Admin_Return_Repair.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Return_Repair.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_txtRepairKeyPressed

    
