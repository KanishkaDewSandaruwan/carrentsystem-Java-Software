    public void get_table(){
        
        int c;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/carrent","root","");
            pst = (PreparedStatement) con.prepareStatement("select * from repair");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rm = rs.getMetaData();
            c = rm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)table1.getModel();
            df.setRowCount(0);
            
            while (rs.next()){
                Vector vector = new Vector();
                
                for (int i = 0; i <= c; i++) {
                    vector.add(rs.getString("repair_id"));
                    vector.add(rs.getString("car_id"));
                    vector.add(rs.getString("car_number"));
                    vector.add(rs.getString("send_date"));
                    vector.add(rs.getString("resive_date"));
                    vector.add(rs.getString("repaired_place"));
                    vector.add(rs.getString("repai_bil_amount"));
                    vector.add(rs.getString("bill_number"));
                    vector.add(rs.getString("finish"));
                }
                df.addRow(vector);
            }
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Admin_Return_Repair.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Return_Repair.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    

    
